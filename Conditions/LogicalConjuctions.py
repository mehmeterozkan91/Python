# Mantıksal bağlaçlar

# and operatörü
result = (1 == 1) and ("M" == "M")
print("and sonucu", result)

# or operatörü
result = (1 == 1) or ("M" == "M")
print("or sonucu", result)

# not operatörü
result = not (1 == 1)
print("not sonucu", result)

#karışık kullanma
result = ((1 == 1) or ("M" == "M") ) and 3 < 4
print("sonucu", result)

