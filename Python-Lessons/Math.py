a = 4
b = 3

print("Toplama: ", a + b)
print("Tam sayı bölmesi: ", a // b)
print("Çıkarma: ", a - b)
print("Çarpma: ", a * b)
print("Bölme: ", a / b)
print("Mod alma (Kalanı bulma): ", a % b)
print("Üssünü bul: ", a ** b)

print("Kare kök bulma: ", a ** 0.5)
print("Küp kök bulma: ", 8 ** (1 / 3))

# Operatörleri beraber kullanma
# Öncelik sıralaması: Parantez, çarpma, bölme, toplama, çıkarma
# İşlemler soldan sağa değerlendirilir.

print("Sonuç: ", 8 + 4 * 4 / 2 - 18)
print("Sonuç: ", ((8 + 4) * (4 / 2)) - 18)
